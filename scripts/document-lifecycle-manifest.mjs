import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join, relative, resolve } from "node:path";
import process from "node:process";

const root = process.cwd();
const docsRoot = resolve(root, "docs");
const outputPath = resolve(docsRoot, "DOCUMENT_LIFECYCLE_MANIFEST.json");
const archiveIndexPath = resolve(docsRoot, "DOCUMENT_ARCHIVE_INDEX.md");
const write = process.argv.includes("--write");

const activeAuthorities = new Set([
  "README.md",
  "AGENTS.md",
  "CHANGELOG.md",
  "docs/DOCUMENTATION_INDEX.md",
  "docs/CURRENT_RELEASE_STATE.md",
  "docs/FULL_SYSTEM_AUDIT_2026_07_16.md",
  "docs/CODEX_BACKLOG_2026_07_16.md",
  "docs/architecture.md",
  "docs/data-strategy.md",
  "docs/roadmap.md",
  "docs/qa-checklist.md",
  "docs/SUPABASE_DATA_API_CONTAINMENT_RUNBOOK_2026_07_16.md"
]);

const excludedDirectories = new Set([
  resolve(root, ".git"),
  resolve(root, ".next"),
  resolve(root, "node_modules"),
  resolve(root, "artifacts")
]);

function collectMarkdown(directory) {
  return readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const absolute = join(directory, entry.name);
    if (entry.isDirectory()) return excludedDirectories.has(absolute) ? [] : collectMarkdown(absolute);
    return entry.isFile() && entry.name.endsWith(".md") ? [absolute] : [];
  });
}

function lifecycleFor(path) {
  if (activeAuthorities.has(path)) return "active_authority";
  if (path === "docs/DOCUMENT_ARCHIVE_INDEX.md") return "generated_navigation";
  if (/\/(?:CR_DEV8_001|SPATIAL_B1_)/.test(path)) return "scoped_release_control_evidence";
  if (/\/(?:RELEASE_|CURRENT_RELEASE_STATE_|CHECKPOINT_|AUDIT_|DEMO_RC_|MVP_)/.test(path)) return "historical_release_evidence";
  if (/(?:_CHANGE_REQUEST|_QA_CHECKLIST|_PLAN|_SPEC|_REVIEW)\.md$/.test(path)) return "historical_or_scoped_delivery_evidence";
  return "historical_or_scoped_reference";
}

function successorFor(path, lifecycle) {
  if (lifecycle === "active_authority") return null;
  if (lifecycle === "historical_release_evidence" || lifecycle === "scoped_release_control_evidence") {
    return "docs/CURRENT_RELEASE_STATE.md";
  }
  if (lifecycle === "historical_or_scoped_delivery_evidence") {
    return "docs/CODEX_BACKLOG_2026_07_16.md";
  }
  return "docs/DOCUMENTATION_INDEX.md";
}

function ruleFor(lifecycle) {
  if (lifecycle === "active_authority") {
    return "Must carry Status, Last verified, Owner, Authority and Successor fields and be updated with affected behavior.";
  }
  if (lifecycle === "generated_navigation") {
    return "Generated sidecar navigation only; the Documentation Index remains authoritative.";
  }
  return "Point-in-time or scoped evidence; follow the recorded successor before using it as current truth.";
}

const paths = collectMarkdown(root)
  .map((absolute) => relative(root, absolute).replaceAll("\\", "/"))
  .sort();
const documents = Object.fromEntries(paths.map((path) => {
  const lifecycle = lifecycleFor(path);
  const currentAuthority = lifecycle === "active_authority";
  return [path, {
    lifecycle,
    currentAuthority,
    successor: successorFor(path, lifecycle),
    rule: ruleFor(lifecycle)
  }];
}));

const manifest = {
  schemaVersion: "1.1",
  lastVerified: "2026-07-16",
  authority: "docs/DOCUMENTATION_INDEX.md",
  counts: {
    markdownDocuments: paths.length,
    activeAuthorities: paths.filter((path) => activeAuthorities.has(path)).length,
    nonActiveDocuments: paths.filter((path) => !activeAuthorities.has(path)).length
  },
  documents
};
const expected = `${JSON.stringify(manifest, null, 2)}\n`;

function archiveLink(path) {
  return path.startsWith("docs/") ? path.slice("docs/".length) : `../${path}`;
}

function successorLink(path) {
  if (!path) return "—";
  const label = path.replace(/^docs\//, "");
  return `[${label}](${archiveLink(path)})`;
}

const archiveRows = Object.entries(documents).map(([path, document]) =>
  `| [\`${path}\`](${archiveLink(path)}) | \`${document.lifecycle}\` | ${document.currentAuthority ? "yes" : "no"} | ${successorLink(document.successor)} |`
);
const archiveExpected = `# GeoAI Document Archive Index

Status: Generated navigation; not an independent authority
Last verified: 2026-07-16
Owner: GeoAI Documentation Governance
Authority: Generated clickable lifecycle inventory; precedence remains with \`DOCUMENTATION_INDEX.md\`
Successor: \`DOCUMENTATION_INDEX.md\`
Navigation: [Documentation Index](DOCUMENTATION_INDEX.md) · [Current Release State](CURRENT_RELEASE_STATE.md) · [Full System Audit](FULL_SYSTEM_AUDIT_2026_07_16.md) · [Codex Backlog](CODEX_BACKLOG_2026_07_16.md)

This sidecar gives every in-scope Markdown file a clickable lifecycle and successor without rewriting historical evidence. It is generated by \`npm run docs:lifecycle:generate\`; do not hand-edit it. Current-truth precedence remains with the [Documentation Index](DOCUMENTATION_INDEX.md).

Counts: **${manifest.counts.markdownDocuments} total · ${manifest.counts.activeAuthorities} active authorities · ${manifest.counts.nonActiveDocuments} non-active/generated records**.

| Document | Lifecycle | Current authority | Successor |
| --- | --- | --- | --- |
${archiveRows.join("\n")}
`;

if (write) {
  writeFileSync(outputPath, expected);
  writeFileSync(archiveIndexPath, archiveExpected);
  console.log(`Wrote ${relative(root, outputPath)} and ${relative(root, archiveIndexPath)} for ${paths.length} Markdown documents.`);
  process.exit(0);
}

if (!existsSync(outputPath) || readFileSync(outputPath, "utf8") !== expected || !existsSync(archiveIndexPath) || readFileSync(archiveIndexPath, "utf8") !== archiveExpected) {
  console.error("Documentation lifecycle manifest is missing or stale. Run npm run docs:lifecycle:generate.");
  process.exit(1);
}

console.log(`Documentation lifecycle manifest passed: ${manifest.counts.activeAuthorities} active, ${manifest.counts.nonActiveDocuments} non-active, ${manifest.counts.markdownDocuments} total.`);
